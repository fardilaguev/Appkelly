package co.edu.ue.appkelly.model;

public class Vehicle {
    private String veh_plate;
    private String mod_model;
    private String use_name;

    public String getVeh_plate() {
        return veh_plate;
    }

    public void setVeh_plate(String veh_plate) {
        this.veh_plate = veh_plate;
    }

    public String getMod_model() {
        return mod_model;
    }

    public void setMod_model(String mod_model) {
        this.mod_model = mod_model;
    }

    public String getUse_name() {
        return use_name;
    }

    public void setUse_name(String use_name) {
        this.use_name = use_name;
    }
}
