package co.edu.ue.appkelly.model;

public class Registration {
    private String reg_id;
    private String reg_date;
    private String veh_id;
    private String tyre_id;
    private String veh_plate;
    private String use_name;
    private String tyre_type;

    public String getReg_id() {
        return reg_id;
    }

    public void setReg_id(String reg_id) {
        this.reg_id = reg_id;
    }

    public String getReg_date() {
        return reg_date;
    }

    public void setReg_date(String reg_date) {
        this.reg_date = reg_date;
    }

    public String getVeh_id() {
        return veh_id;
    }

    public void setVeh_id(String veh_id) {
        this.veh_id = veh_id;
    }

    public String getTyre_id() {
        return tyre_id;
    }

    public void setTyre_id(String tyre_id) {
        this.tyre_id = tyre_id;
    }

    public String getVeh_plate() {
        return veh_plate;
    }

    public void setVeh_plate(String veh_plate) {
        this.veh_plate = veh_plate;
    }

    public String getUse_name() {
        return use_name;
    }

    public void setUse_name(String use_name) {
        this.use_name = use_name;
    }

    public String getTyre_type() {
        return tyre_type;
    }

    public void setTyre_type(String tyre_type) {
        this.tyre_type = tyre_type;
    }
}
